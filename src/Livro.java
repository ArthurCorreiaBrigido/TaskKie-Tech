public class Livro {

    private String título;
    private String autor;
    private String editora;
    private String ano_de_publicacao;
    private String codigoSBN;

    public String getTítulo() {
        return título;
    }

    public void setTítulo(String título) {
        this.título = título;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getEditora() {
        return editora;
    }

    public void setEditora(String editora) {
        this.editora = editora;
    }

    public String getAno_de_publicacao() {
        return ano_de_publicacao;
    }

    public void setAno_de_publicacao(String ano_de_publicacao) {
        this.ano_de_publicacao = ano_de_publicacao;
    }

    public String getCodigoSBN() {
        return codigoSBN;
    }

    public void setCodigoSBN(String codigoSBN) {
        this.codigoSBN = codigoSBN;
    }
}
